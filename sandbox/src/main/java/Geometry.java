public class Geometry {
    public static void main(String[] args) {
        System.out.println("Площадь квадрата со стороной " + 2 + " = " + (2*2));
        var squareSide = 7.;
        // var - переменная, а тип double
        //чтобы показать джаве, что число должно быть не int, а double, можно написать 7.0 или просто 7.
        System.out.println("Площадь квадрата со стороной " + squareSide + " = " + (squareSide * squareSide));
    }
}